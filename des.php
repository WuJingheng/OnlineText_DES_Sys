//XOR运算
function calculateXOR($str1,$str2){
  $arrTmpXOR = Array();
  for ($tmp=0; $tmp < 48; $tmp++) {
    $arrTmpXOR[$tmp] = ((int)substr($str1, $tmp, 1) + (int)substr($str2, $tmp, 1)) % 2;
  }
  return join('',$arrTmpXOR);
}

